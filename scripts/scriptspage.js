function showHide(){
            if(document.getElementById("esc1").style.display == 'block'){
                document.getElementById("esc1").style.display = 'none'
                document.getElementById("esc2").style.display = 'none'
                document.getElementById("esc3").style.display = 'none'
                document.getElementById("esc4").style.display = 'none'

            }else{
                document.getElementById("esc1").style.display = 'block'
                document.getElementById("esc2").style.display = 'block'
                document.getElementById("esc3").style.display = 'block'
                document.getElementById("esc4").style.display = 'block'
            }
}